
a = input("Please Choose One +,-, /, x")
if a == "+":
    st = int(input("Put in your first number"))
    sc = int(input("Put in your second number"))
    print(st + sc)   
elif a == "-":
     st = int(input("Put in your first number"))
     sc = int(input("Put in your second number"))
     print(st - sc)
elif a == "/":
      st = int(input("Put in your first number"))
      sc = int(input("Put in your second number"))
      print(st / sc)
elif a == "x":
      st = int(input("Put in your first number"))
      sc = int(input("Put in your second number"))
      print(st * sc)
   
    
